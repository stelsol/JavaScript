function Carregar(){
    // por mais que os inputs sejam number os numeros que est칚o armazenados nas variaveis est칚o como string.
   let ini = document.getElementById('txti')
   let fim = document.getElementById('txtf')
   let pass = document.getElementById('txtp')
   let res = document.getElementById('res')

   if(ini.value.length == 0 || fim.value.length == 0 || pass.value.length == 0){
        //N칚o permite 0 e nem que os forms fiquem vazios, caso ocorra ser치 acionado um alerta.
        alert('[ERRO!!] nenhum dado inserido')
   } else {
        res.innerHTML = 'Contando: <br>'
         //est치 pr칩ximas variaveis ser칚o para fazer a convers칚o do texto para os numeros.
        let i = Number(ini.value)
        let f = Number(fim.value) 
        let p = Number(pass.value)
        if(p <= 0){
            alert('P = 0 칠 invalido trocando para p = 1')
            p = 1
        }
        if( i < f){
            //contagem crescente 
            for( let c = i ; c <= f ; c += p ){
                res.innerHTML += ` ${c}游녤`
            }  
        } else {
            //contagem descrecente
            for(let c = i ; c >= f ; c -= p){
                res.innerHTML += ` ${c}游녤 `
            }
        }
        res.innerHTML +=`游끠`
   }
}


